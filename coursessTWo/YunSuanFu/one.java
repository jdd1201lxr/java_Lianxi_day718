public class one {
    //算数运算符
    /**
    加+、减-、*乘、除/、求余数%
     除法得到是商、取余得到的是余数
     整数相除，只能得到整数，要想得到小数，要有浮点型参与
     */
    public static void main(String[] args) {
        int i = 9;
        int b = 8;
        System.out.println(i+b);
        System.out.println(i-b);
        System.out.println(i*b);
        System.out.println(i/b);
        System.out.println(i%b);

        System.out.println(3.0/2.0);
    }
}
