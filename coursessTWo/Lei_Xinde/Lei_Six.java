public class Lei_Six {
    public static void main(String[] args) {
        /**
         成员变量和局部变量
         成员变量:在类当中,方法体之外的变量就是成员变量--实例变量(局部变量存储在堆内存当中)
         局部变量:在方法中的变量就是局部变量(局部变量存储在栈内存当中)

         两者区别:
         位置不同(作用域不同)成员变量可以全局通用,局部变量只能在方法中使用
         存储地址不同:局部变量存储在堆内存当中,局部变量存储在栈内存当中
         生命周期不同:成员变量是随着对象的存在而存在,随着对象的消失而消失,局部变量是随着方法的调用而存在,随着方法的调用完毕而消失
         初始化值不同:成员变量有默认值,局部变量没有初始化默认值,必须先定义,赋值才能使用。
         */
    }

}
